      bw_hist$written) / (2 * 86400), variable = "bwhist"))
  date_breaks <- date_breaks(
    as.numeric(max(as.Date(bandwidth$date, "%Y-%m-%d")) -
    min(as.Date(bandwidth$date, "%Y-%m-%d"))))
