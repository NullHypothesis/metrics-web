        minor = date_breaks$minor) +
    scale_y_continuous(name = "", limits = c(0, max(u$relays,
        na.rm = TRUE)), formatter = formatter) +
    opts(title = title)
  ggsave(filename = path, width = 8, height = 5, dpi = 72)
}

